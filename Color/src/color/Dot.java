package color;

public class Dot {

	private int xcoor;
	private int ycoor;
	
	public Dot(int xcoor, int ycoor) {
		super();
		this.xcoor = xcoor;
		this.ycoor = ycoor;
	}

	@Override
	public String toString() {
		return "Dot [xcoor=" + xcoor + ", ycoor=" + ycoor + "]";
	}

	
	
}


